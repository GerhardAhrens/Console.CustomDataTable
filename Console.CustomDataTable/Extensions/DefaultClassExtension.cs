namespace Console.CustomDataTable
{
    using System;
    using System.CodeDom;
    using System.CodeDom.Compiler;
    //using System.Data;

    using Microsoft.CSharp;

    public static class DefaultClassExtension
    {

        public static string DefaultClassToCode(this string self)
        {
            string className = self;
            if (string.IsNullOrWhiteSpace(className) == true)
            { 
                className = "UnnamedTableAsClass";
            }

            className = $"{className}";

            // Create the class
            CodeTypeDeclaration codeClass = CreateClass(className);

            // Add Class to Namespace
            string namespaceName = "AutoGeneratedModel";
            CodeNamespace codeNamespace = new CodeNamespace(namespaceName);
            codeNamespace.Types.Add(codeClass);

            // Add Methode ToString()
            codeClass.Members.Add(CreateMethode("ToString", new CodeTypeReference(typeof(string)), MemberAttributes.Public | MemberAttributes.Override));

            // Add Methode GetInfo()
            codeClass.Members.Add(CreateMethode("GetInfo1", new CodeTypeReference(typeof(string))));

            codeClass.Members.Add(CreateMethode2("GetInfo2", new CodeTypeReference("ResultInfo")));

            // Generate code
            string filename = $"{namespaceName}.{className}.cs";
            CreateCodeFile(filename, codeNamespace);

            // Return filename
            return filename;
        }

        private static CodeTypeDeclaration CreateClass(string name)
        {
            CodeTypeDeclaration result = new CodeTypeDeclaration(name);
            result.Attributes = MemberAttributes.Public;
            result.IsClass = true;
            result.Members.Add(CreateConstructor(name)); // Add Klassenkonstruktor

            return result;
        }

        private static CodeConstructor CreateConstructor(string className)
        {
            CodeConstructor result = new CodeConstructor();
            result.Attributes = MemberAttributes.Public;
            result.Name = className;

            return result;
        }

        private static CodeMemberMethod CreateMethode(string methodeName, CodeTypeReference returnType, MemberAttributes attributes = MemberAttributes.Public | MemberAttributes.Final)

        {
            CodeMemberMethod resultMethod = new CodeMemberMethod();
            resultMethod.Name = methodeName;
            resultMethod.ReturnType = returnType;
            resultMethod.Attributes = attributes;
            resultMethod.Statements.Add(new CodeMethodReturnStatement(new CodePrimitiveExpression("Hallo aus der generierten Klasse")));
            return resultMethod;
        }

        private static CodeMemberMethod CreateMethode2(string methodeName, CodeTypeReference returnType, MemberAttributes attributes = MemberAttributes.Public | MemberAttributes.Final)

        {
            CodeMemberMethod resultMethod = new CodeMemberMethod();
            resultMethod.Name = methodeName;
            resultMethod.ReturnType = returnType;
            resultMethod.Attributes = attributes;

            CodeVariableDeclarationStatement varDecl = new CodeVariableDeclarationStatement("ResultInfo", "res", new CodeObjectCreateExpression("ResultInfo"));
            resultMethod.Statements.Add(varDecl);

            CodePropertyReferenceExpression propertyRef = new CodePropertyReferenceExpression(new CodeVariableReferenceExpression("res"), "Name");
            CodeAssignStatement assignStmt = new CodeAssignStatement(propertyRef, new CodePrimitiveExpression("Inhalt gef³llt via CodeDom"));
            resultMethod.Statements.Add(assignStmt);
            resultMethod.Statements.Add(new CodeMethodReturnStatement(new CodeVariableReferenceExpression("res")));

            return resultMethod;
        }

        private static void CreateCodeFile(string filename, CodeNamespace codeNamespace)
        {
            CodeGeneratorOptions codeOptions = new CodeGeneratorOptions();
            codeOptions.BlankLinesBetweenMembers = false;
            codeOptions.VerbatimOrder = true;
            codeOptions.BracingStyle = "C";
            codeOptions.IndentString = "\t";

            // Schreibe Source in File
            using (TextWriter textWriter = new StreamWriter(filename))
            {
                CSharpCodeProvider codeProvider = new CSharpCodeProvider();
                codeProvider.GenerateCodeFromNamespace(codeNamespace, textWriter, codeOptions);
            }

            // Korrektur des Autoproperty Trick (wegen Semikolon)
            string source = File.ReadAllText(filename).Replace("//;", string.Empty);
            File.WriteAllText(filename, source);
        }
    }
}
